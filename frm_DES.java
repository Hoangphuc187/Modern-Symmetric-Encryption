    private void btn_allActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_allActionPerformed
        // TODO add your handling code here:
         try {
     
        BufferedReader brEnc = new BufferedReader(new FileReader("D:\\EnDes.txt"));
        StringBuilder sbEnc = new StringBuilder();
        char[] caEnc = new char[5];
        while (brEnc.ready()) {
            int len = brEnc.read(caEnc);
            sbEnc.append(caEnc, 0, len);
        }
        brEnc.close();
        txt_cip.setText(sbEnc.toString());

    
        BufferedReader brDec = new BufferedReader(new FileReader("D:\\DeDes.txt"));
        StringBuilder sbDec = new StringBuilder();
        char[] caDec = new char[5];
        while (brDec.ready()) {
            int len = brDec.read(caDec);
            sbDec.append(caDec, 0, len);
        }
        brDec.close();
        txt_plain.setText(sbDec.toString()); 

        JOptionPane.showMessageDialog(null, "Đã hiển thị nội dung của file mã hóa và giải mã!!");
    } catch (IOException ex) {
        Logger.getLogger(frm_DES.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Lỗi khi đọc file!!");
    }
    }//GEN-LAST:event_btn_allActionPerformed

