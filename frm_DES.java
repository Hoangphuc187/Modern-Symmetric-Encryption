    private void btn_hideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hideActionPerformed
        // TODO add your handling code here:
          try {
        BufferedReader br = null;
        String fileName = "D:\\EnDes.txt";
        br = new BufferedReader(new FileReader(fileName));
        StringBuilder sb = new StringBuilder();
       
        if (br == null) {
            JOptionPane.showMessageDialog(null, "Đã mở file!!!");
        }

        char[] ca = new char[5];
        while (br.ready()) {
            int len = br.read(ca);
            sb.append(ca, 0, len);
        }
        br.close();

        String chuoi = sb.toString();
        txt_cip.setText(chuoi);
    } catch (IOException ex) {
        Logger.getLogger(frm_DES.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btn_hideActionPerformed

